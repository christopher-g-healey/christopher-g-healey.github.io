#!/usr/bin/python

#  Mathematical operation

print( 2 + 5 )

# Lists

l = [ 1, 2, 3 ]
print( l )
print( l[ 0 ] )

# Dictionaries

email = { 'healey': 'healey@ncsu.edu', 'rappa': 'mrappa@ncsu.edu' }
print( email )
print( email[ 'healey' ] )

# Sets

basket = [ 'orange', 'apple', 'pear', 'apple', 'durian', 'orange' ]
fruit = set( basket )
print( fruit )
print( 'durian' in fruit )
print( 'pomegranate' in fruit )
